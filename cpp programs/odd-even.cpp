//WAP to find if number is even or odd(Use of if-else)
# include <iostream>
using namespace std;

int a;
int main()
{
  cout <<"Enter a number:";
  cin >> a;

if ((a%2)==0)
{
   cout <<"\nThe entered number number is even";
}

else
{
   cout <<"\nThe entered number is odd";
}

return 0;
}
